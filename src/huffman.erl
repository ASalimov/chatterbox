encode(<<>>, Acc) when bit_size(Acc) < 8 ->
    Padding = 8 rem bit_size(Acc),
    encode(<<>>, Acc, Padding);
encode(<<>>, Acc) ->
    Padding = bit_size(Acc) rem 8,
    encode(<<>>, Acc, Padding).
encode(<<>>, Acc, Padding) ->
    Bits = round(math:pow(2,Padding) - 1),
    <<Acc/bits,Bits:Padding>>.
